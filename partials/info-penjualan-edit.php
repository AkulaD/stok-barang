<section class="readme">
    <h3>Penjelasan</h3>
    <ul>
        <li>
            <h4>1.Today's Statistics</h4>
            <p>
                Bagian ini menampilkan ringkasan statistik transaksi penjualan pada hari ini. Informasi yang ditampilkan meliputi
                jumlah barang terjual (Total Items Sold), total pendapatan (Total Revenue), jumlah transaksi (Total Transactions),
                serta jumlah transaksi yang masih membutuhkan pembaruan harga (Need Update / TRX).
            </p>
        </li>

        <li>
            <h4>2.Filter & Search Transaction</h4>
            <p>
                Bagian ini berfungsi untuk memfilter data transaksi berdasarkan rentang tanggal, status update harga, serta pencarian
                produk atau lokasi transaksi. Fitur ini memudahkan pengguna untuk menemukan transaksi tertentu yang perlu dilakukan
                pembaruan harga.
            </p>
        </li>

        <li>
            <h4>3.List Transactions</h4>
            <p>
                Bagian ini menampilkan daftar transaksi penjualan yang harganya belum diperbarui. Informasi yang ditampilkan meliputi
                waktu transaksi, nama produk, harga sementara, lokasi transaksi (misalnya hasil scan barcode), serta status update.
            </p>
        </li>

        <li>
            <h4>4.Edit Transaction</h4>
            <p>
                Pada setiap transaksi tersedia tombol "Edit" yang berfungsi untuk memperbarui harga produk.
                Fitur ini digunakan ketika transaksi dilakukan menggunakan scan barcode namun harga belum tersimpan atau perlu
                disesuaikan dengan harga terbaru produk.
            </p>
        </li>

        <li>
            <h4>5.Update Status Selected</h4>
            <p>
                Bagian ini memungkinkan pengguna untuk memilih beberapa transaksi sekaligus dan memperbarui statusnya secara massal.
                Fitur ini sangat membantu untuk mempercepat proses update harga pada banyak transaksi dalam satu waktu.
            </p>
        </li>
    </ul>

    <div class="readme-box">
        Pastikan setiap transaksi yang berstatus <b>No Update</b> segera diperbarui agar laporan penjualan dan total pendapatan
        tetap akurat dan sesuai dengan harga produk yang berlaku.
    </div>
</section>
